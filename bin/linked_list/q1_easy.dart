
// What is a linked list, and how does it differ from an array?
//
//A linked list is a dynamic data structure consisting of nodes, where each node contains two parts:
//
// Data – The actual value stored in the node.
// Pointer – A reference to the next node in the sequence.

//Feature	                   Linked List	                                            Array
// Memory Allocation	       Dynamic (nodes allocated as needed)	                    Static (fixed size or contiguous block)
// Insertion/Deletion	       Efficient (O(1) at the beginning, O(n) in worst case)  	Costly (shifting elements needed)
// Access Time	             O(n) (sequential access)                             	  O(1) (direct access via index)
// Memory Usage	             Extra memory for pointers	                              No extra memory needed
// Data Storage	             Non-contiguous                                         	Contiguous
