// What is a stack, and how does it follow the LIFO principle?

//A stack is a data structure that stores elements in a specific order, following the Last In, First Out (LIFO) principle.
// This means that the last element added to the stack is the first one to be removed.
//
// Imagine a stack as a collection of items arranged in a vertical order, where you can only add or remove items from the top.
// When you add an item to the stack, it is placed on top of the existing items. To remove an item, you remove the topmost item.
//
