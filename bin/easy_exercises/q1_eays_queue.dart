// What is a queue, and how does it follow the FIFO principle?

// What is a queue?
// A queue is a linear data structure that follows a specific order in which elements are added and removed.
// It works similarly to a real-world queue (like a line of people waiting for a service),
// where new elements are added at one end (rear) and removed from the other end (front).
//
// How does it follow the FIFO principle?
// The FIFO (First In, First Out) principle means that the first element added to the queue is the first one to be removed.
// This is similar to a queue of customers: the first customer in line is served first.
//
// Enqueue (Insertion) → Adds an element at the rear of the queue.
// Dequeue (Removal) → Removes an element from the front of the queue.
// Example:
// If you enqueue A, B, and C in that order, when you dequeue, the elements come out in the same order: A first, then B, then C.