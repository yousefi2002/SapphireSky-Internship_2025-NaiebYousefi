// What is the difference between a queue and a stack?

// 1. Definition
// Queue: A linear data structure that follows the FIFO (First In, First Out) principle.
// Stack: A linear data structure that follows the LIFO (Last In, First Out) principle.

// 2. Insertion and Removal
// Queue:
// Insertion (enqueue) happens at the rear.
// Removal (dequeue) happens from the front.
// Stack:
// Insertion (push) happens at the top.
// Removal (pop) happens from the top.

// 3. Basic Operations
// Queue:
// enqueue(data) → Adds data to the rear.
// dequeue() → Removes data from the front.
// peek() → Returns the front element without removing it.
// Stack:
// push(data) → Adds data to the top.
// pop() → Removes data from the top.
// peek() → Returns the top element without removing it.

// 4. Order of Processing
// Queue: First element added is the first to be removed (FIFO).
// Stack: Last element added is the first to be removed (LIFO).

// 5. Usage Examples
// Queue:
// Task scheduling (CPU scheduling, printer queue).
// Call center system (first caller gets served first).
// Breadth-First Search (BFS) in graphs.

// Stack:
// Undo/Redo functionality in text editors.
// Function call stack (used in recursion).
// Browser history (back/forward navigation).